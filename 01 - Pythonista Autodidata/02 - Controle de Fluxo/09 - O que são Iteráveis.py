# O que é um ITERÁVEL?
# ------------------------------------------------------------------------------------


# É um tipo de dado que nos permite passar por cada valor dentro dele.
# 
# Exemplos de iteráveis:
# 
# -> range()
# -> lista[]
# -> String


# Uma STRING também é um iterável:
nome = 'Ronaldinho Gaúcho'
