# Operadores Lógicos

# comparações NUMÉRICAS
idade = 15
print(idade > 17)  # False
print(idade < 17)  # True
print(idade <= 17)  # True
print(idade >= 17)  # False
print(idade == 17)  # False
print(idade != 17)  # True

# comparações entre outros TIPOS
print(True == True)  # True
print('Rafael' == 'rafael')  # False
print('b' > 'a')  # True
print(5 == '5')  # False
