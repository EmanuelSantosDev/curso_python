# Conversão de Tipos


# convertendo para INTEIRO
idade = 36
print(int(idade) > 18)  # True


# convertendo para STRING
titulos = 5
print(type(titulos))  # <class 'int'>
titulos_str = str(titulos)
print(type(titulos_str))  # <class 'str'>


# convertendo para FLOAT
nota = 37
nota_2 = float(nota)
print(type(nota_2))  # <class 'float'>


